package se.Iths;

public class Calculator {

    public int Add(int num1 , int num2){
        return num1 + num2;

    }

    public int Sub(int num1 , int num2){
        return num1 - num2;
    }

    public int Multiplication(int num1 , int num2){
        return num1 * num2;
    }

    public int Division(int num1 , int num2){
       return num1 / num2;
    }
}
